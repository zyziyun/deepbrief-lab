        "            except Exception as e:\n"
        "                content = json.dumps({\"success\": False, \"error\": str(e)})\n"
        "            messages.append({\"role\": \"tool\", \"tool_call_id\": tc.id, \"content\": content})\n"
        "\n"
        "    # Max steps hit — force a final text answer instead of raising\n"
        "    messages.append({\n"
        "        \"role\": \"user\",\n"
        "        \"content\": \"Max steps reached. Provide your best answer based on what you've learned. Do not call any more tools.\",\n"
        "    })\n"
        "    final = await client.chat.completions.create(model=model, messages=messages, tool_choice=\"none\")\n"
        "    return {\n"
        "        \"answer\": final.choices[0].message.content,\n"
        "        \"steps\": max_steps,\n"
        "        \"trace\": trace,\n"
        "        \"terminated_by\": \"max_steps\",\n"
