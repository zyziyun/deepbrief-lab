        "            \"tokens\": resp.usage.total_tokens,\n"
        "            \"tool_calls\": [tc.function.name for tc in (msg.tool_calls or [])],\n"
        "            \"content\": msg.content,\n"
        "        })\n"
        "\n"
        "        # Natural exit — no more tool calls\n"
        "        if not msg.tool_calls:\n"
        "            return {\"answer\": msg.content, \"steps\": step + 1, \"trace\": trace, \"terminated_by\": \"natural\"}\n"
        "\n"
        "        # Execute tool calls — convert any error into an observation the LLM can read\n"
        "        for tc in msg.tool_calls:\n"
        "            try:\n"
        "                args = json.loads(tc.function.arguments)\n"
