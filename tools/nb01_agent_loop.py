        "    if system:\n"
        "        messages.append({\"role\": \"system\", \"content\": system})\n"
        "    messages.append({\"role\": \"user\", \"content\": user_msg})\n"
        "    trace = []\n"
        "\n"
        "    for step in range(max_steps):\n"
        "        resp = await client.chat.completions.create(\n"
        "            model=model,\n"
        "            messages=messages,\n"
